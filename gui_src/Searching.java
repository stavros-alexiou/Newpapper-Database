    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost3306/newspapper", "root", "Stav23019900275");
            
            String sql = "select * from employee where email = ?";
            PreparedStatement pstmt = (PreparedStatement) conn.prepareStatement(sql);
            pstmt.setString(1, email.getText());
            
            ResultSet rs =  (ResultSet) pstmt.executeQuery();
            if(rs.next()) 
            {
                name.setText(rs.getString("name"));
                lastname.setText(rs.getString("last_name"));
                password.setText(rs.getString("password"));
                salary.setText(String.valueOf(rs.getInt("salary")));
                hireDate.setText(rs.getString("hire_date"));
                papper.setText(rs.getString("newspapper_name"));
            } 
            else
            {
                JOptionPane.showMessageDialog(null, "Record Not Found.");
            }
            
            conn.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

