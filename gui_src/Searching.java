    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/newspapper", "root", "Stav23019900275");
            
            String sql = "update employee set name = ?, lastname = ?, password = ?, hireDate = ?, papper = ? where email = ?";
            
            PreparedStatement pstmt = (PreparedStatement) conn.prepareStatement(sql);
            pstmt.setString(1, name.getText());
            pstmt.setString(2, lastname.getText());
            pstmt.setString(3, password.getText());
            pstmt.setString(4, String.valueOf(salary.getText()));
            pstmt.setString(5, hireDate.getText());
            pstmt.setString(6, papper.getText());
            pstmt.setString(7, email.getText());
            pstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Record Updated Successfully.");
            
            conn.close();
        }  
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

